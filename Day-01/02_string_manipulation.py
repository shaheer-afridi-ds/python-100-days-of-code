# 1. New Line Escape Sequence

# using `\n` to add a new line to strings.

print("Hello")
print("Word")

# is the same as:

print("Hello\nWorld")

# 2. String Concatenation
# Use the plus symbol `+` to concatenate different strings together

print("My name is" + " " + "Shaheer")

# will output: "My name is Shaheer"
